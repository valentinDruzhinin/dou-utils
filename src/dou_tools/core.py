def add(a, b):
    """Функція для додавання двох чисел

    Параметри:
    a (int): перше число
    b (int): друге число

    Повертає:
    int: сума двох чисел
    """
    return a + b


def substract(a, b):
    """Функція віднімання одного числа від іншого

    Параметри:
    a (int): перше число
    b (int): друге число

    Повертає:
    int: різниця двох чисел
    """
    return a - b
